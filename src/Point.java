
public class Point {
    double x;
    double y;

    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    void setX(double x) {
        this.x = x;
    }

    double getX() {
        return (this.x);
    }

    void setY(double y) {
        this.y = y;
    }

    double getY() {
        return (this.y);
    }

}
